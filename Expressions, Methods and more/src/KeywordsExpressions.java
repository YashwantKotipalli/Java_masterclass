public class KeywordsExpressions {

    public static void main(String[] args ){

        // 1 mile = 1.609344 kilometers

        double miles = 100d;
        double kilometers = 1.609344d*miles;
        // datatype expression
        // expression in java consists of variables, values and operators
        // adding a datatype to an expression and a semicolon at the end of an expression makes it a statement in java
        System.out.println(kilometers);

        int highscore = 50;
        if(highscore >=50){
            System.out.println("This is an expression as well");
        }




    }
}
